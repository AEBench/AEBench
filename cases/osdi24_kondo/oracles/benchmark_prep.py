from __future__ import annotations

from collections.abc import Sequence
from dataclasses import dataclass
from pathlib import Path

from evaluator.oracles.case_base import CaseOracleBenchmarkPrepBase
from evaluator.oracles.env_setup_checks import FilesystemPathCheck, PathType

from evaluator.oracles import utils

from .common import PROTOCOLS

_AUTOGEN_FILE = "messageInvariantsAutogen.dfy"

_REQUIRED_SYNC_FILES = ("applicationProof.dfy", "spec.dfy", "system.dfy", "verify")
_REQUIRED_MANUAL_FILES = ("applicationProof.dfy", "spec.dfy", "verify")
_REQUIRED_ASYNC_KONDO_FILES = ("distributedSystem.dfy", "spec.dfy", "verify")


@dataclass(frozen=True, slots=True, kw_only=True)
class ProtocolSourceFilesCheck(utils.BaseCheck):
	"""Fail if any protocol is missing required .dfy source files."""

	protos_dir: Path

	def check(self, *_args, **_kwargs) -> utils.CheckResult:
		missing: list[str] = []

		for protocol in PROTOCOLS:
			proto_dir = self.protos_dir / protocol

			for filename in _REQUIRED_SYNC_FILES:
				path = proto_dir / "sync" / filename
				if not path.exists():
					missing.append(f"{protocol}/sync/{filename}")

			for filename in _REQUIRED_MANUAL_FILES:
				path = proto_dir / "manual" / filename
				if not path.exists():
					missing.append(f"{protocol}/manual/{filename}")

			for filename in _REQUIRED_ASYNC_KONDO_FILES:
				path = proto_dir / "async-kondo" / filename
				if not path.exists():
					missing.append(f"{protocol}/async-kondo/{filename}")

		if missing:
			shown = missing[:15]
			more = f" ... ({len(missing) - 15} more)" if len(missing) > 15 else ""
			return utils.CheckResult.failure(
				f"{len(missing)} required source file(s) missing:\n"
				+ "\n".join(f"- {m}" for m in shown)
				+ more
			)

		return utils.CheckResult.success(
			message=f"all {len(PROTOCOLS)} protocols have required source files"
		)


@dataclass(frozen=True, slots=True, kw_only=True)
class AutogenFilesCheck(utils.BaseCheck):
	"""Fail if autogenerated files are missing (verify-all not run)."""

	protos_dir: Path

	def check(self, *_args, **_kwargs) -> utils.CheckResult:
		missing: list[str] = []
		for protocol in PROTOCOLS:
			autogen = self.protos_dir / protocol / "async-kondo" / _AUTOGEN_FILE
			if not autogen.is_file():
				missing.append(protocol)

		if missing:
			return utils.CheckResult.failure(
				f"autogenerated {_AUTOGEN_FILE} missing for "
				f"{len(missing)}/{len(PROTOCOLS)} protocols: " + ", ".join(missing)
			)
		return utils.CheckResult.success(
			message=f"{_AUTOGEN_FILE} present for all {len(PROTOCOLS)} protocols"
		)


class OracleBenchmarkPrep(CaseOracleBenchmarkPrepBase):
	def requirements(self) -> Sequence[utils.BaseCheck]:
		repo_root = self.paths.workspace_dir

		protos_dir = repo_root / "kondoPrototypes"

		return (
			FilesystemPathCheck(
				name="verify_all_script",
				path=protos_dir / "verify-all",
				path_type=PathType.FILE,
			),
			ProtocolSourceFilesCheck(
				name="protocol_source_files",
				protos_dir=protos_dir,
			),
			AutogenFilesCheck(
				name="autogen_files_exist",
				protos_dir=protos_dir,
			),
		)
